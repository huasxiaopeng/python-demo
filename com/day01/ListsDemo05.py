# 列表深入学习
cars=['bmw','audi','toyota','byd','furte','das auto']
for c in cars:
    print(c)


for cc in cars:
    print(cc.upper()+"全部变成大写")

# 只要缩进是其意思，可以无限进行写语句
for cc in cars:
    print(cc.upper()+"全部变成大写\n")
    print(cc.lower()+"全部变成小写")



# 循环结束

print("全部执行完毕了。。。")



